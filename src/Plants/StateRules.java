package Plants;

public class StateRules {
	private int time;
	private int minWater;
	private int maxWater;
	private int minAbono;
	private int maxAbono;
	private int effect;
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public int getMinWater() {
		return minWater;
	}
	public void setMinWater(int minWater) {
		this.minWater = minWater;
	}
	public int getMaxWater() {
		return maxWater;
	}
	public void setMaxWater(int maxWater) {
		this.maxWater = maxWater;
	}
	public int getMinAbono() {
		return minAbono;
	}
	public void setMinAbono(int minAbono) {
		this.minAbono = minAbono;
	}
	public int getMaxAbono() {
		return maxAbono;
	}
	public void setMaxAbono(int maxAbono) {
		this.maxAbono = maxAbono;
	}
	public int getEffect() {
		return effect;
	}
	public void setEffect(int effect) {
		this.effect = effect;
	}

}
